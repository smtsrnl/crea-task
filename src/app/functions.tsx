export function randomString(len: number) {
    let charSet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
    var randomString = '';
    for (var i = 0; i < len; i++) {
        var randomPoz = Math.floor(Math.random() * charSet.length);
        randomString += charSet.substring(randomPoz, randomPoz + 1);
    }
    return randomString;
}

export function calculatedRate(array?: ProductCommentModel[]) {

    let commentsSize = array?.length || 0;

    let rate = 0;

    array?.map((comment: ProductCommentModel) => {
        rate += comment.rate;
    });

    let calculatedRate = rate / commentsSize || 0;

    return calculatedRate;
}


